/*
  Appends a new token with the given string repesentation to the end of the list of tokens.
  @param str the string representation of the token
*/
void TokenList::append(const string &str) {
  Token *token = new Token(str);
  append(token);
}

/*
  Appends the token to the end of the list of tokens. token must not be NULL.
  @param token the token to add to the list
*/
void TokenList::append(Token *token) {
  if (token == NULL) {
  	std::cout << "TokenList::append received a NULL ptr" << std::endl;
    return;
  }

  // this is the first token in the list
  if (head == NULL) {
    head = token;
    tail = token;
    nextToken = token;
  } else {
    tail->next = token;
    token->prev = tail;
    token->next = NULL;
    tail = token;
  }
}

/*
  Deletes a token from the list of tokens.  If the token is not found in the
  list, the list is not modified. token must not be NULL.
  @param token the token to delete from the list
*/
void TokenList::deleteToken(Token *token) {
  if (token == NULL) {
    return; // we are not allowed cout or exit so return quiety
  }

  if (token == head && token == tail) {
    // only one token in the list all feilds must be updated
    head = NULL;
    tail = NULL;
    nextToken = NULL;

    delete token;
  } else if (token == head) {
    // delete the head token
    head = token->next;
    head->prev = NULL;

    if (token == nextToken)
    {
      // update the nextToken to be taken by getNextToken()
      nextToken = head;
    }

    delete token;
  } else if (token == tail) {
    // delete the tail token
    tail = token->prev;
    tail->next = NULL;

    if (token == nextToken) {
      // update the nextToken to be taken by getNextToken()
      nextToken = tail;
    }
    delete token;
  } else {
    // delete tokens in the middle of the linked list
    Token *prev = token->prev;
    Token *next = token->next;
    prev->next = next;
    next->prev = prev;

    if (token == nextToken) {
     // update the nextToken to be taken by getNextToken()
      nextToken = next;
    }

    delete token;
  }
}

/*
  This function is reponsible for processing a command at point in the string.
  @param command the command that needs to be performed
*/
void Tokenizer::processStateCommand(commands_t command) {
  switch (command) {
  case STORE: 
    // append the current character to the end of the buffer
    buffer += cur_char;
    offset++;
    break;

  case ACCEPT: 
    // append the current character to the buffer, then create a token using the buffer as the stringRep
    addToken();
    offset++;
    break;

  case ACCEPT_BACK1: 
    // ignore the current character, then create a token using the buffer as the stringRep
    state = START;
    tokens.append(buffer);
    buffer.clear();
    break;

  case ACCEPT_BACK2: 
    // ignore the current character and prev character,  then create a token using the buffer as the stringRep
    state = START;
    tokens.append(buffer);
    buffer.clear();
    offset--;
    break;

  case START_BLOCK_COMMENT:
    // append the current character to the end of the buffer, then create a token with buffer as stringRep
    processingBlockComment = true;
    addToken();
    offset++;
    break;

  case START_INLINE_COMMENT: 
    // append the current character to the end of the buffer, then create a token with buffer as sringRep
    processingInlineComment = true;
    addToken();
    offset++;
    break;

  case START_INCLUDE: 
    // append the current character to the end of the buffer, then create a token with buffer as stringRep
    processingIncludeStatement = true;
    addToken();
    offset++;
    break;

  case STOP_BLOCK_COMMENT: 
    // This command never got used in the final solution but i will leave it here
    tokens.append(buffer.substr(0, buffer.length() - 2));
    buffer.clear();
    processingBlockComment = false;
    offset++;
    break;

  case IGNORE: 
    // all spaces, and tabs are ignored, we only save them to a buffer when inside a comment, string, or include <>
    state = START;
    offset++;
    break;

  default: 
    // the program should never reach this state.
    state = START;
    cout << "Invalid State" << endl;
    exit(1);
    break;
  }
}

/*
  Takes the buffer and creates a token.  The token is then added to the list of token inside
  Tokenizer class.
*/
void Tokenizer::addToken() {
  // after we find a token sucessfully the program goes back to state zero
  state = START; 
  buffer += cur_char;
  tokens.append(buffer);
  buffer.clear();
}

/*
  Takes a string and tokenizes it.  Each token is first added to a list inside 
  the Tokenizer class. When all tokens are added from a line they can be retrieved 
  by the getNextToken function one-by-one.
  @param str the string to be tokenized.  Not allowed to be NULL.
*/
void Tokenizer::setString(string *str) {
  if (str == NULL) {
  	std::cout << "Tokenizer::setString recieved a NULL input" << std::endl;
    return; // return quiety
  }

  this->str = str;
  // a space is appended on the end to allow the machine to properly detect a
  // number at the end of a line
  str->append(" ");
  buffer.clear();
  offset = START;
  commands_t command;
  tokens.deleteList(); // clear out the internal list of tokens

  //Read characters one-by-one from the string and process a command at each step
  while (offset < str->length()) {
    cur_char = str->at(offset);

    if (processingBlockComment) {
      if (cur_char == '*' && str->at(offset + 1) == '/') {
        // reached the end of a block comment
        processingBlockComment = false;

        if (buffer.length() != 0) {
          // don't append tokens when the buffer is empty
          tokens.append(buffer);
          buffer.clear();
        }
      } else if (offset == str->length() - 1) {
        // added a space so we ignore it when reading a comment
        if (buffer.length() != 0) {
          // don't append tokens when the buffer is empty
          tokens.append(buffer);
          buffer.clear();
        }
        break;
      } else {
        buffer += cur_char;
        offset++;
      }
    } else if (processingInlineComment) {
      if (offset == str->length() - 1) {
        // added a space so we ignore it when reading a comment
        tokens.append(buffer);
        buffer.clear();
        processingInlineComment = false;
        break;
      } else {
        buffer += cur_char;
        offset++;
        continue;
      }
    } else if (processingIncludeStatement) {
      // When in a preprocessor statement we make the '<' '>' behave like
      // a quotation mark We do that pretending we saw a quotation instead of a < or >
      if (cur_char == '<' || cur_char == '>') {
        state = transitionTable[(int)'"'][state];
      } else {
        state = transitionTable[cur_char][state];
      }
      command = stateCommands[state];
      processStateCommand(command);
      if (offset == str->length() - 1) {
        // once the end of the line is reached the include statement is over. minus
        // one because we added a whitespace

        processingIncludeStatement = false;
      }
    } else {
      state = transitionTable[cur_char][state];
      command = stateCommands[state];
      processStateCommand(command);
    }
  }

  // if no tokens were added to the tokenizer internal list. this was an empty
  // string or eof
  if (tokens.getFirst() == NULL) {
    complete = true;
  } else {
    complete = false;
  }
}

/*
  Returns the next token in the list.  Empty string if there are no more
  tokens in the list
  @returns a string representation of the token
*/
string Tokenizer::getNextToken() {
  Token *t = tokens.getNextInList();

  if (t == NULL) {
    complete = true;
    return "";
  }

  if (t->getNext() == NULL) {
    complete = true;
  }

  return t->getStringRep();
}
